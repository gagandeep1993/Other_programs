public class Pojo_kav
{
    public static float pi= 4.5F;
    private int id;
    private int age;
    private double height;
    private String name;

    public static float getPi() {
        return pi;
    }

    public static void setPi(float pi) {
        Pojo_kav.pi = pi;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
